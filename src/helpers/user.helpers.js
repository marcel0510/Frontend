export const RoleMap = (role) => {
    switch(role){
        case 0:
            return "Administrador";
        case 1:
            return "C. Eléctrica";
        case 2:
            return "C. Control";
        case 3:
            return "C. Telecomunicaciones"
        case 4:
            return "C. Tecnologías de la Información"

    }
}